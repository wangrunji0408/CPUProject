;测试扩展指令
;循环 0x8000次

START:
	LI R4 0x0
	LI R5 0x80
	SLL R5 R5 0x0
LOOP:
	NOT R2 R4 ;R2=1xxxxxxxxx
	NOT R3 R5 ;R3=01111...11
	SLT R2 R3
	BTEQZ 0x70
	NOP
	SLL R0 R5 0x0 
	SLT R2 R5
	BTEQZ NEWP
	NOP
	B 0x70
	NOP
NEWP:
	SRA R1 R3 0x0 ;R1=0111,1111
	SLTUI R1 0x7F
	BTEQZ NEWPP
	NOP
	B 0x70
	NOP
NEWPP:
	ADDIU R4 0x1
	SRL R2 R4 0x0  
	SRL R2 R2 0x5  ;R2=100,when R4=1000,0000,...
	SLTUI R2 0x4
	BTEQZ END
	NOP
	B LOOP
	SW_RS 0x0
END:
	JR R7 ; 结束时寄存器 R1=007F R2=0004 R3=7FFF R4=8000 R5=8000
	NOP



